// Build the ⌘K search index from the markdown content: one entry per page
// (frontmatter title) plus one per `##` heading. Runs before dev/build —
// authoring a page IS indexing it.
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const here = path.dirname(fileURLToPath(import.meta.url));
const contentDir = path.join(here, "..", "..", "docs");

const slugifyHeading = (text) => text.toLowerCase().replace(/[^\w]+/g, "-").replace(/^-+|-+$/g, "");
const plain = (md) => md.replace(/`([^`]*)`/g, "$1").replace(/[*_]/g, "").trim();

const entries = [];
for (const file of fs.readdirSync(contentDir).filter((f) => f.endsWith(".md") && f !== "README.md").sort()) {
  const src = fs.readFileSync(path.join(contentDir, file), "utf8");
  const slug = file.replace(/\.md$/, "");
  const fm = /^---\n([\s\S]*?)\n---\n/.exec(src);
  const meta = Object.fromEntries(
    (fm?.[1] ?? "").split("\n").flatMap((l) => {
      const kv = /^([\w-]+):\s*(.*)$/.exec(l.trim());
      return kv ? [[kv[1], kv[2]]] : [];
    }),
  );
  const title = meta.title ?? slug;
  const group = meta.group ?? "Guide";
  const body = fm ? src.slice(fm[0].length) : src;
  entries.push({ href: `/${slug}`, title, group });
  // Headings — but never inside fenced code blocks.
  let inFence = false;
  for (const line of body.split("\n")) {
    if (/^```/.test(line)) inFence = !inFence;
    if (inFence) continue;
    const h = /^##\s+(.+)$/.exec(line);
    if (!h) continue;
    const heading = plain(h[1]);
    entries.push({ href: `/${slug}#${slugifyHeading(heading)}`, title, group, heading });
  }
}

const out = path.join(here, "..", "src", "app", "search-index.ts");
fs.writeFileSync(
  out,
  "// GENERATED by scripts/search-index.mjs — do not edit.\nexport default " + JSON.stringify(entries, null, 2) + " as const;\n",
);
console.log(`search-index: ${entries.length} entries → src/app/search-index.ts`);
