// Build the ⌘K search index from the page sources: one entry per page (title)
// plus one per <h2> heading. Runs automatically before dev/build (predev /
// prebuild) — authoring a page IS indexing it.
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const here = path.dirname(fileURLToPath(import.meta.url));
const pagesDir = path.join(here, "..", "src", "app", "pages");
const layout = fs.readFileSync(path.join(here, "..", "src", "app", "layout.tsx"), "utf8");

// page href + group + label from NAV_GROUPS in layout.tsx
const groups = [...layout.matchAll(/group: "([^"]+)",\s*pages: \[([\s\S]*?)\]/g)];
const nav = groups.flatMap(([, group, body]) =>
  [...body.matchAll(/href: "([^"]+)", label: "([^"]+)"/g)].map(([, href, label]) => ({ group, href, label })),
);

/** Strip JSX down to readable text: tags out, string-expressions inlined. */
const textOf = (jsx) =>
  jsx
    .replace(/\{"\s*"\}/g, " ")
    .replace(/\{`([^`]*)`\}/g, "$1")
    .replace(/\{"((?:[^"\\]|\\.)*)"\}/g, "$1")
    .replace(/<[^>]+>/g, "")
    .replace(/&amp;/g, "&")
    .replace(/\s+/g, " ")
    .trim();

const entries = [];
for (const file of fs.readdirSync(pagesDir).filter((f) => f.endsWith(".tsx"))) {
  const src = fs.readFileSync(path.join(pagesDir, file), "utf8");
  const active = /active="([^"]+)"/.exec(src)?.[1];
  const page = nav.find((n) => n.href === active);
  if (!page) continue;
  const href = `/${page.href}`;
  entries.push({ href, title: page.label, group: page.group });
  for (const [, h2] of src.matchAll(/<h2>([\s\S]*?)<\/h2>/g)) {
    const heading = textOf(h2);
    if (!heading) continue;
    const id = heading.toLowerCase().replace(/[^\w]+/g, "-").replace(/^-+|-+$/g, "");
    entries.push({ href: `${href}#${id}`, title: page.label, group: page.group, heading });
  }
}

const out = path.join(here, "..", "src", "app", "search-index.ts");
fs.writeFileSync(out, "// GENERATED by scripts/search-index.mjs — do not edit.\nexport default " + JSON.stringify(entries, null, 2) + " as const;\n");
console.log(`search-index: ${entries.length} entries → ${path.relative(process.cwd(), out)}`);
